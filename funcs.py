		"—É–∑–±–µ–∫–∏—Å—Ç–∞–Ω", "—Ç–∞–¥–∂–∏–∫–∏—Å—Ç–∞–Ω", "–∞–∑–µ—Ä–±–∞–π–¥–∂–∞–Ω", "—Å–µ—Ä–±–∏—è", "—Ç—É—Ä—Ü–∏—è", "—Å–Ω–≥", "–µ–∞—ç—Å",
